public class TypeCasting {
    public static void main(String[] args) {

        double d = 12.75;
        int i = (int)d;

        System.out.println("Double: " + d);
        System.out.println("Int: " + i);

        int num = 25;
        double d2 = num;

        System.out.println("Integer: " + num);
        System.out.println("Double: " + d2);
    }
}